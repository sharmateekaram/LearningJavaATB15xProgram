package ex_09_While_Loop;

public class Lab00_WhileLoop_Simple {
    static void main(String[] args) {
        int i = 0; // Init
        while (i < 10) {//condition
            System.out.println(i);
            i++; // Updation
        }

        //  I, C, U
        for (int j = 0; j < 10 ; j++) {
            System.out.println(j);
        }
    }
}
